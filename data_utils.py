#用来生成dataset，Dataloader会利用这个类里定义的__getitem__和__len__拉提取一批又一批的batch
class TextAudioLoader(torch.utils.data.Dataset):
    """
        1) loads audio, text pairs
        2) normalizes text and converts them to sequences of integers
        3) computes spectrograms from audio files.
    """
    def __init__(self, audiopaths_and_text, hparams):
        self.audiopaths_and_text = load_filepaths_and_text(audiopaths_and_text)
        self.text_cleaners  = hparams.text_cleaners#english_cleaners2
        self.max_wav_value  = hparams.max_wav_value#32768.0
        self.sampling_rate  = hparams.sampling_rate#16000
        self.filter_length  = hparams.filter_length#1024
        self.hop_length     = hparams.hop_length#256
        self.win_length     = hparams.win_length#1024
        # 下面的写法意思是，如果hparams里有对应超参数，就直接赋值，如果没有就用最后的数值False、1、200
        self.cleaned_text = getattr(hparams, "cleaned_text", False)#这个是有的，所以按照超参数定义的值，应该是True
        self.add_blank = hparams.add_blank#True
        self.min_text_len = getattr(hparams, "min_text_len", 1)#这个是超参数设置文件里没有的，所以是1
        self.max_text_len = getattr(hparams, "max_text_len", 200)#这个是超参数设置文件里没有的，所以是200
        random.seed(1234)
        random.shuffle(self.audiopaths_and_text)
        # print('过滤前的列表长度：', len(self.audiopaths_and_text)) #训练集4702，测试集588
        self._filter()


    def _filter(self):
        """
        Filter text & store spec lengths
        """
        # Store spectrogram lengths for Bucketing
        # wav_length ~= file_size / (wav_channels * Bytes per dim) = file_size / (1 * 2)
        # spec_length = wav_length // hop_length

        audiopaths_and_text_new = []
        lengths = []
        for audiopath, text in self.audiopaths_and_text:
            # 只选择长度在合适范围内的样本，这就是过滤（这个长度包含这段话里的字母和空格，比如“short text”的长度是10）
            if self.min_text_len <= len(text) and len(text) <= self.max_text_len:
                audiopaths_and_text_new.append([audiopath, text]) #[['DUMMY1/SP02-OTR002-02-B-135.wav', 'ཆབ་སྲིད་འཁོར་བའི་བཙོན་ར་ཡིན།'],......[],......]
                lengths.append(os.path.getsize(audiopath) // (2 * self.hop_length))
        self.audiopaths_and_text = audiopaths_and_text_new
        self.lengths = lengths
        # print('过滤后的self.audiopaths_and_text长度:', len(self.audiopaths_and_text))
        # print('过滤后的self.lengths:', self.lengths)
        # print(self.audiopaths_and_text[0])

    def get_audio_text_pair(self, audiopath_and_text):
        # separate filename and text
        audiopath, text = audiopath_and_text[0], audiopath_and_text[1]
        text = self.get_text(text)
        spec, wav = self.get_audio(audiopath)
        # 这里的text已经根据词汇表转换成id序列；spec是计算出来的wav对应的快速傅里叶变换的幅度谱；wav是直接load wav文件得到的张量数据
        return (text, spec, wav)

    def get_audio(self, filename):
        # 读取wav文件返回对应的采样率（self.sampling_rate）和读取到的wav对应的数据（这个数据被转换成floatensor了，也就是张量）
        audio_norm, sampling_rate = load_wav_to_torch(filename, self.sampling_rate)
        if sampling_rate != self.sampling_rate:
            raise ValueError("{} {} SR doesn't match target {} SR".format(
                sampling_rate, self.sampling_rate))
        #audio_norm = audio / self.max_wav_value
        audio_norm = audio_norm.unsqueeze(0)#在最前面增加一个维度
        spec_filename = filename.replace(".wav", ".spec.pt")
        if os.path.exists(spec_filename):
            spec = torch.load(spec_filename)
        else:
            spec = spectrogram_torch(audio_norm, self.filter_length,
                self.sampling_rate, self.hop_length, self.win_length,
                center=False)
            spec = torch.squeeze(spec, 0)#去掉增加的一个维度
            torch.save(spec, spec_filename)#保存为指定文件spec.pt
        #返回的audio_norm是直接处理得到的张量样本数据，spec是对应wav的快速傅里叶变换的幅度谱
        return spec, audio_norm

    #将文本转换成id序列，并且添加0作为序列间隔的id，返回处理后的结果序列
    def get_text(self, text):
        if self.cleaned_text:#True
            text_norm = cleaned_text_to_sequence(text)#文本转序列
        else:
            text_norm = text_to_sequence(text, self.text_cleaners)
        if self.add_blank:#True
            # commons.intersperse的功能是在原本序列的开头结尾都加一个0，然后在每个序列之间都加一个0，比如[1,2,3]变成[0,1,0,2,0,3,0]
            text_norm = commons.intersperse(text_norm, 0)
        text_norm = torch.LongTensor(text_norm)
        return text_norm

    def __getitem__(self, index):
        return self.get_audio_text_pair(self.audiopaths_and_text[index])

    def __len__(self):
        return len(self.audiopaths_and_text)

#每一批提取出来的batch输入，经过_call_处理以后得到补零对齐以后的新batch，输出用来生成真正的dataloader
class TextAudioCollate():
    """ Zero-pads model inputs and targets
    """
    def __init__(self, return_ids=False):
        self.return_ids = return_ids

    def __call__(self, batch):
        """Collate's training batch from normalized text and aduio
        PARAMS
        ------
        batch: [text_normalized, spec_normalized, wav_normalized]
        """
        # Right zero-pad all one-hot text sequences to max input length
        # x[1].size(1)就是spec的[freauencies,time_step]里的time_step，按照time_step从大到小进行排序，返回的ids_sorted_decreasing是排序后的索引
        _, ids_sorted_decreasing = torch.sort(
            torch.LongTensor([x[1].size(1) for x in batch]),
            dim=0, descending=True)

        max_text_len = max([len(x[0]) for x in batch])
        max_spec_len = max([x[1].size(1) for x in batch])
        max_wav_len = max([x[2].size(1) for x in batch])

        text_lengths = torch.LongTensor(len(batch))
        spec_lengths = torch.LongTensor(len(batch))
        wav_lengths = torch.LongTensor(len(batch))

        #按照最大的长度构建初始化的全零张量
        text_padded = torch.LongTensor(len(batch), max_text_len)
        spec_padded = torch.FloatTensor(len(batch), batch[0][1].size(0), max_spec_len)
        wav_padded = torch.FloatTensor(len(batch), 1, max_wav_len)
        text_padded.zero_()
        spec_padded.zero_()
        wav_padded.zero_()
        for i in range(len(ids_sorted_decreasing)):
            row = batch[ids_sorted_decreasing[i]]
            #给全零张量里对应的样本的那一行数据进行赋值，同时记录text长度 
            text = row[0]
            text_padded[i, :text.size(0)] = text
            text_lengths[i] = text.size(0)
            #同上
            spec = row[1]
            spec_padded[i, :, :spec.size(1)] = spec
            spec_lengths[i] = spec.size(1)
            #同上
            wav = row[2]
            wav_padded[i, :, :wav.size(1)] = wav
            wav_lengths[i] = wav.size(1)

        if self.return_ids:
            return text_padded, text_lengths, spec_padded, spec_lengths, wav_padded, wav_lengths, ids_sorted_decreasing
        return text_padded, text_lengths, spec_padded, spec_lengths, wav_padded, wav_lengths


"""Multi speaker version"""
class TextAudioSpeakerLoader(torch.utils.data.Dataset):
    """
        1) loads audio, speaker_id, text pairs
        2) normalizes text and converts them to sequences of integers
        3) computes spectrograms from audio files.
    """
    def __init__(self, audiopaths_sid_text, hparams):
        self.audiopaths_sid_text = load_filepaths_and_text(audiopaths_sid_text)
        self.text_cleaners = hparams.text_cleaners
        self.max_wav_value = hparams.max_wav_value
        self.sampling_rate = hparams.sampling_rate
        self.filter_length  = hparams.filter_length
        self.hop_length     = hparams.hop_length
        self.win_length     = hparams.win_length
        self.sampling_rate  = hparams.sampling_rate

        self.cleaned_text = getattr(hparams, "cleaned_text", False)

        self.add_blank = hparams.add_blank
        self.min_text_len = getattr(hparams, "min_text_len", 1)
        self.max_text_len = getattr(hparams, "max_text_len", 190)

        random.seed(1234)
        random.shuffle(self.audiopaths_sid_text)
        self._filter()

    def _filter(self):
        """
        Filter text & store spec lengths
        """
        # Store spectrogram lengths for Bucketing
        # wav_length ~= file_size / (wav_channels * Bytes per dim) = file_size / (1 * 2)
        # spec_length = wav_length // hop_length

        audiopaths_sid_text_new = []
        lengths = []
        for audiopath, sid, text in self.audiopaths_sid_text:
            if self.min_text_len <= len(text) and len(text) <= self.max_text_len:
                audiopaths_sid_text_new.append([audiopath, sid, text])
                lengths.append(os.path.getsize(audiopath) // (2 * self.hop_length))
        self.audiopaths_sid_text = audiopaths_sid_text_new
        self.lengths = lengths

    def get_audio_text_speaker_pair(self, audiopath_sid_text):
        # separate filename, speaker_id and text
        audiopath, sid, text = audiopath_sid_text[0], audiopath_sid_text[1], audiopath_sid_text[2]
        text = self.get_text(text)
        spec, wav = self.get_audio(audiopath)
        sid = self.get_sid(sid)
        return (text, spec, wav, sid)

    def get_audio(self, filename):
        audio, sampling_rate = load_wav_to_torch(filename)
        if sampling_rate != self.sampling_rate:
            raise ValueError("{} {} SR doesn't match target {} SR".format(
                sampling_rate, self.sampling_rate))
        audio_norm = audio / self.max_wav_value
        audio_norm = audio_norm.unsqueeze(0)
        spec_filename = filename.replace(".wav", ".spec.pt")
        if os.path.exists(spec_filename):
            spec = torch.load(spec_filename)
        else:
            spec = spectrogram_torch(audio_norm, self.filter_length,
                self.sampling_rate, self.hop_length, self.win_length,
                center=False)
            spec = torch.squeeze(spec, 0)
            torch.save(spec, spec_filename)
        return spec, audio_norm

    def get_text(self, text):
        if self.cleaned_text:
            text_norm = cleaned_text_to_sequence(text)
        else:
            text_norm = text_to_sequence(text, self.text_cleaners)
        if self.add_blank:
            text_norm = commons.intersperse(text_norm, 0)
        text_norm = torch.LongTensor(text_norm)
        return text_norm

    def get_sid(self, sid):
        sid = torch.LongTensor([int(sid)])
        return sid

    def __getitem__(self, index):
        return self.get_audio_text_speaker_pair(self.audiopaths_sid_text[index])

    def __len__(self):
        return len(self.audiopaths_sid_text)


